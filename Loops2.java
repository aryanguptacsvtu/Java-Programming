public class Loops2 {
    public static void main(String[] args) {

        int n = 10;
        System.out.println("Using FOR loop:-");
        System.out.println("Incrementing For loop:");
        for (int i = 0; i < n; i++) {
            System.out.println(2 * i + 1);
        }

        System.out.println("\nDecrementing For loop:");
        for (int i = 8; i > 0; i--) {
            System.out.println(i);
        }
    }
}
